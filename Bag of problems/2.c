#include <stdio.h>
int main()
{
int a; double b; float c; char ch;
scanf ("%d", &a);
printf ("The integer number is %d\n", a);
double ;
scanf ("%lf", &b);
printf ("The double number is %lf\n", b);
float ;
scanf ("%f", &c);
printf ("The float number is %f\n", c);
char ;
scanf ("%c", &ch);
printf ("The character is %c\n", ch);
return 0;
}
